    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        final String email;
        final String password;
        if (jCheckBox2.isSelected()) {
            email = settings.get("email");
            password = settings.get("emailpassword");
        } else {
            email = jTextField1.getText();
            password = jPasswordField1.getText();
        }
        try {
            Properties props = new Properties();
            props.put("mail.smtp.host", "smtp.gmail.com");
            props.put("mail.smtp.socketFactory.port", "465");
            props.put("mail.smtp.socketFactory.class",
                    "javax.net.ssl.SSLSocketFactory");
            props.put("mail.smtp.auth", "true");
            props.put("mail.smtp.port", "465");
            
            Session session = Session.getDefaultInstance(props,
                    new javax.mail.Authenticator() {
                        protected PasswordAuthentication getPasswordAuthentication() {
                            return new PasswordAuthentication(
                                    email,
                                    password);
                        }
                    });
            
            Message message = (Message) new MimeMessage(session);
            message.setFrom(new InternetAddress(
                    email));
            message.setRecipients(Message.RecipientType.TO,
                    InternetAddress.parse(jTextField2.getText()));
            message.setSubject(jTextArea2.getText());
            
            if (attatchmentFile != null) {
                MimeBodyPart messageBodyPart = new MimeBodyPart();
                messageBodyPart.setText(jTextArea1.getText());
                Multipart multipart = new MimeMultipart();
                multipart.addBodyPart(messageBodyPart);
                messageBodyPart = new MimeBodyPart();
                DataSource source = new FileDataSource(attatchmentFile.getAbsolutePath());
                messageBodyPart.setDataHandler(new DataHandler(source));
                messageBodyPart.setFileName(attatchmentFile.getName());
                multipart.addBodyPart(messageBodyPart);
                message.setContent(multipart);
            } else {
                message.setText(jTextArea1.getText());
            }
            Transport.send(message);
            JOptionPane.showMessageDialog(EmailSender.this,
                    "Email to: " + jTextField2.getText(),
                    "Your email was sent",
                    JOptionPane.INFORMATION_MESSAGE);
            logger.info("Email to " + jTextField2.getText() + " successfuly sent");
        } catch (Exception e) {
            logger.warn(e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(EmailSender.this,
                    "Check your internet connection or your parameters", "Error Message",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

