    class WebcamThread extends Thread {

        @Override
        public void run() {
            try {
                grabber = new OpenCVFrameGrabber(0);
                grabber.start();
                opencv_core.IplImage image = grabber.grab();
                while (true && StartingSettings.this.isVisible() && image != null) {
                    originalImage = grabber.grab();
                    jLabel1.setIcon(new ImageIcon(originalImage.getBufferedImage()));
                    StartingSettings.this.revalidate();
                    StartingSettings.this.repaint();
                }
            } catch (Exception ex) {
                logger.warn(ex.getMessage());
                ex.printStackTrace();
            }
        }

    }

