    public void activateTab(int tabIndex) {
        for (int i = 0; i < 5; i++) {
            if (i > tabIndex) {
                jTabbedPane1.setEnabledAt(i, false);
            } else {
                jTabbedPane1.setEnabledAt(i, true);
            }
        }

        jTabbedPane1.setSelectedIndex(tabIndex);
    }

